package com.icarus.tutorial.j8.mref;

public class MyMathUtils {

	// Method này có 2 tham số a và b, và trả về giá trị int
	// Method này khác tên nhưng có cấu trúc giống method trừu tượng
	// định nghĩa trong interface MyFunction (Functional Interface)
	public static int sum(int a, int b) {
		return a + b;
	}

	// Method này có 2 tham số a và b, và trả về giá trị int
	// Method này khác tên nhưng có cấu trúc giống method trừu tượng
	// định nghĩa trong interface MyFunction (Functional Interface)
	public static int minus(int a, int b) {
		return a - b;
	}

}
